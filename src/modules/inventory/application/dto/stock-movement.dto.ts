import type { MovementType, MovementStatus } from "../../domain/entities/stock-movement.entity";

/**
 * API Response DTOs for Stock Movements
 */

export interface MovementLineResponseDto {
  id: string;
  productId: string;
  productName: string;
  productSku: string;
  quantity: number;
  unitCost: number | null;
}

export interface StockMovementResponseDto {
  id: string;
  warehouseId: string;
  warehouseName: string;
  type: MovementType;
  status: MovementStatus;
  reference: string | null;
  reason: string | null;
  note: string | null;
  lines: MovementLineResponseDto[];
  createdBy: string;
  createdAt: string;
  postedAt: string | null;
}

export interface StockMovementListResponseDto {
  data: StockMovementResponseDto[];
  pagination: {
    page: number;
    limit: number;
    total: number;
    totalPages: number;
  };
}

export interface CreateMovementLineDto {
  productId: string;
  quantity: number;
  unitCost?: number;
}

export interface CreateStockMovementDto {
  warehouseId: string;
  type: MovementType;
  reference?: string;
  reason?: string;
  note?: string;
  lines: CreateMovementLineDto[];
}

export interface StockMovementFilters {
  warehouseId?: string;
  type?: MovementType;
  status?: MovementStatus;
  productId?: string;
  startDate?: string;
  endDate?: string;
  search?: string;
  page?: number;
  limit?: number;
}
